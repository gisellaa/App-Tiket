    private void btnKonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonfirmasiActionPerformed
        TiketLogik T = new TiketLogik();
        tujuan=(String) cmbTujuan.getSelectedItem();
        if ((txtID.getText().equals("")) || (txtNama.getText().equals("")) ||
                (txtPenumpang.getText().equals("")) ||
                (txtBagasi.getText().equals("")) || (tujuan=="Pilih Tujuan")) {
            JOptionPane.showMessageDialog(null, "Data masih kosong!","Invalid",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            T.setId(txtID.getText());
            T.setNama(txtNama.getText());
            T.setTujuan(tujuan);
            T.setBagasi(Integer.parseInt(txtBagasi.getText()));
            T.setPenumpang(Integer.parseInt(txtPenumpang.getText()));
            txtSubtotal.setText(String.valueOf(T.Subtotal()));
            txtPajak.setText(String.valueOf(T.Pajak()));
            txtTotal.setText(String.valueOf(T.Total()));
            
            btnKonfirmasi.setEnabled(true);
            btnSimpan.setEnabled(true);
            btnEdit.setEnabled(false);
            btnHapus.setEnabled(false);
            btnBatal.setEnabled(true);
        }
    }//GEN-LAST:event_btnKonfirmasiActionPerformed


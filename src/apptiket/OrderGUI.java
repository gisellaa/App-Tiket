    public void tampilData(String where) {
        try {
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM data " + where);
            while (rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("nama"),
                    rs.getString("tujuan"),
                    rs.getString("penumpang"),
                    rs.getString("bagasi"),
                    rs.getString("subtotal"),
                    rs.getString("pajak"),
                    rs.getString("total_hrg"),
                };
                tabModel.addRow(data);
            }
        }catch(Exception e) {
            e.printStackTrace();
        }
    }
    
    public Boolean cariData(String where){
        try {
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM data " + where);

            while (rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("nama"),
                    rs.getString("tujuan"),
                    rs.getString("penumpang"),
                    rs.getString("bagasi"),
                    rs.getString("subtotal"),
                    rs.getString("pajak"),
                    rs.getString("total_hrg"),
                };
                tabModel.addRow(data);
                return (true);
            }
            return (false);
        }catch(Exception e) {
            e.printStackTrace();
        }return(false);
    }


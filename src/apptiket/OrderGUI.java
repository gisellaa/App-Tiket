    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            st = cn.createStatement();
            if (edit) { //jika edit true
                st.executeUpdate("UPDATE data set " + "id='" + txtID.getText() +
                        "'," + "nama='" + txtNama.getText() + "'," +
                        "penumpang='" +txtPenumpang.getText()+ "', " + "tujuan='" +
                        cmbTujuan.getSelectedItem()+ "', "+ "bagasi='" +
                        txtBagasi.getText() + "'," + "subtotal='" +
                        txtSubtotal.getText() + "'," + "pajak='" +
                        txtPajak.getText() + "'," + "total_hrg='" +
                        txtTotal.getText()+ "' WHERE id ='" + sId + "' ");
                JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");
                tampilData("");
            }else { // jika edit false
                if (cariData(" WHERE id like  '%"+txtID.getText()+"%'")){
                    JOptionPane.showMessageDialog(null, "Kode tiket sudah ada!",
                            "Invalid", JOptionPane.ERROR_MESSAGE);
                    tampilData("");
                }else {
                    st.executeUpdate("INSERT INTO data VALUES ('"+txtID.getText()+
                        "','"+txtNama.getText()+"','"+txtPenumpang.getText()+
                        "','"+cmbTujuan.getSelectedItem()+"','"+txtBagasi.getText()+
                        "','"+txtSubtotal.getText()+"','"+txtPajak.getText()+
                        "','"+txtTotal.getText()+"')");
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
                    tampilData("");
                }
            }
            refresh();
            } catch (Exception e) {
                e.printStackTrace();
            }
    }//GEN-LAST:event_btnSimpanActionPerformed

